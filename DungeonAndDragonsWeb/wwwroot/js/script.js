function NextCell(point, rotate, person, path) {
    if (person.rotate != rotate) {
        person.rotate = rotate;

        setTimeout(() => {
            person.position.x += point.x;
            person.position.y += point.y;
            setTimeout(() => {
                if (path.length != 0) {
                    Move(person, path);
                }
                else {
                   // MoveNextPerson();
                }
            }, 500);
        }, 500);

    }
    else {
        person.position.x += point.x;
        person.position.y += point.y;

        setTimeout(() => {
            if (path.length != 0) {
                Move(person, path);
            }
            else {
                MoveNextPerson();
            }
        }, 500)
    }
}

function getRandomInt(min, max) {
    min = Math.ceil(min);
    max = Math.floor(max);
    return Math.floor(Math.random() * (max - min) + min);
}

function Move(person, path) {
    let step = path.shift();
    let point = { x: 0, y: 0 };
    let rotate = 0;
    
    if (person.position.x > step.x) {

        rotate = 180 * person.direction_x;
        point.x--;
    }
    else if (person.position.x < step.x) {
        rotate = 0;
        point.x++;
    }
    if (person.position.y > step.y) {
        rotate = -90 ;
        point.y--;
    }
    else if (person.position.y < step.y) {
        rotate = 90;
        point.y++;
    }
    NextCell(point, rotate, person, path);
}

